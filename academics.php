                } else {
                    let fd = new FormData();

                    fd.append("name", name);
                    fd.append("email", email);
                    fd.append("grade", grade);
                    fd.append("message", message);


                    $.ajax({
                        url: "email.php",
                        data: fd,
                        contentType: false,
                        processData: false,
                        type: "post",
                        success: function (response) {
                            console.log(response);

